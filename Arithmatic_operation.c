#include<stdio.h>
int main()
{
	int a = 28, b = 12;
	printf("a+b = %d\n", a+b);
	printf("a-b = %d\n", a-b);
	printf("a*b = %d\n", a*b);
	printf("a/b = %d\n", a/b);
	printf("a % b = %d\n", a%b);
	printf("+a = %d\n", +a);
	printf("-a = %d\n", -a);
	printf("++a = %d\n", ++a);
	printf("a-- = %d\n", a--);
	return 0;
	
}
